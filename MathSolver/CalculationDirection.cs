namespace MathSolver
{
    /// <summary>
    /// Represents the direction for evaluating expressions.
    /// </summary>
    public enum CalculationDirection
    {
        /// <summary>
        /// Evaluate expressions from left to right.
        /// </summary>
        LeftToRight,

        /// <summary>
        /// Evaluate expressions from right to left.
        /// </summary>
        RightToLeft
    }
}